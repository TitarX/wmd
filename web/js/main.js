function setBackgroundColor(el1)
{
    $(el1).css('background-color','lemonchiffon');
}
function setBackgroundColor(el1,el2)
{
    $(el1).css('background-color','lemonchiffon');
    $(el2).css('background-color','#EEEEEE');
}

function removeBackgroundColor(el1)
{
    $(el1).css('background-color','#FFFFFF');
}
function removeBackgroundColor(el1,el2)
{
    $(el1).css('background-color','#FFFFFF');
    $(el2).css('background-color','#DDDDDD');
}
